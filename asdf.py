import pyttsx3
import datetime
import speech_recognition as sr 

engine = pyttsx3.init('nsss')
voices = engine.getProperty('voices')
# print(voices[1].id)
engine.setProperty('voice', voices[0].id)


def speak(audio):
    engine.say(audio)
    engine.runAndWait()



